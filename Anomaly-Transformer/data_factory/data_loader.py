class TUBESegLoader(Dataset):
    def __init__(self, data_path, win_size, step, mode="train"):
        self.mode = mode
        self.step = step
        self.win_size = win_size
        self.scaler = StandardScaler()

        # 加载预处理后的 tube_day.csv（无标签）
        df = pd.read_csv(os.path.join(data_path, 'tube1_day_no_label.csv'))
        data = df.values.astype(np.float32)
        data = np.nan_to_num(data)

        self.scaler.fit(data)
        data = self.scaler.transform(data)

        # 划分训练/验证/测试（简单方式：前60%/20%/20%）
        num_total = len(data)
        num_train = int(num_total * 0.6)
        num_val = int(num_total * 0.2)

        self.train = data[:num_train]
        self.val = data[num_train:num_train + num_val]
        self.test = data[num_train + num_val:]

        # 如果你有标签可以加载：
        label_path = os.path.join(data_path, 'tube1_day_label.csv')
        if os.path.exists(label_path):
            labels = pd.read_csv(label_path).values.astype(np.float32)
            self.test_labels = labels[num_train + num_val:]
        else:
            self.test_labels = np.zeros((len(self.test),), dtype=np.float32)

        print(f"tube loader - train: {self.train.shape}, test: {self.test.shape}")

    def __len__(self):
        data_map = {
            'train': self.train,
            'val': self.val,
            'test': self.test
        }
        return (data_map[self.mode].shape[0] - self.win_size) // self.step + 1

    def __getitem__(self, index):
        index = index * self.step
        if self.mode == "train":
            data = self.train[index:index + self.win_size]
            label = np.zeros((self.win_size,), dtype=np.float32)  # 没有标签，也返回 fake 标签
        elif self.mode == 'val':
            data = self.val[index:index + self.win_size]
            label = np.zeros((self.win_size,), dtype=np.float32)
        elif self.mode == 'test':
            data = self.test[index:index + self.win_size]
            label = self.test_labels[index:index + self.win_size]
        else:
            raise ValueError("Unsupported mode")
        return np.float32(data), np.float32(label)
            


def get_loader_segment(data_path, batch_size, win_size=100, step=100, mode='train', dataset='KDD'):
    if (dataset == 'SMD'):
        dataset = SMDSegLoader(data_path, win_size, step, mode)
    elif (dataset == 'MSL'):
        dataset = MSLSegLoader(data_path, win_size, 1, mode)
    elif (dataset == 'SMAP'):
        dataset = SMAPSegLoader(data_path, win_size, 1, mode)
    elif (dataset == 'PSM'):
        dataset = PSMSegLoader(data_path, win_size, 1, mode)
    elif (dataset == 'TUBE'):
        dataset = TUBESegLoader(data_path, win_size, step, mode)

    shuffle = False
    if mode == 'train':
        shuffle = True

    data_loader = DataLoader(dataset=dataset,
                             batch_size=batch_size,
                             shuffle=shuffle,
                             num_workers=0)
    return data_loader
